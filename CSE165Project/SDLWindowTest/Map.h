class Map {
public:
	Map() {		//Map Constructor
		brickFloor = TextureManager::LoadTexture("assets/brick.png");

		Map::loadMap(level1);

		//Rectangle initialization for tile placement
		start.x = 0;
		start.y = 0;
		start.w = dest.w = 32;
		start.h = dest.h = 32;
		dest.x = dest.y = 0;

	}

	~Map() {
		SDL_DestroyTexture(brickFloor);
	}

	//Initialize map with loaded in map

	void loadMap(int arr[20][25]) {
		for (int row = 0; row < 20; row++) {
			for (int column = 0; column < 25; column++) {

				map[row][column] = arr[row][column];	//Place value from index of input array to map array
			}

		}
	}

	//Render map onto screen
	void drawMap() {
		int tileType = 0;

		for (int row = 0; row < 20; row++) {
			for (int column = 0; column < 25; column++) {

				tileType = map[row][column];

				dest.x = column * 32;	//Update placement pixles based on index
				dest.y = row * 32;

				//To render new tiles, add more textures to map constructor and add the to the switch case tree
				switch (tileType) {
				case 0:
					TextureManager::draw(brickFloor, start, dest);
				default:
					break;
				}

			}

		}
	}

private:

	SDL_Rect start, dest;
	SDL_Texture* brickFloor;

	int map[20][25];


};

